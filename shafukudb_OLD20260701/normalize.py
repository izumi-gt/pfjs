# -*- coding: utf-8 -*-
"""
科目名の正規化。照合キーの素を作る唯一の場所。

設計方針:
- 正典側(seiten_master.csv の正規名)と実PDF側の抽出名に「同一の」正規化をかける。
  どちらか一方にだけかけると照合基準がズレるため、必ず両側で本モジュールを通す。
- 旧 shafuku_parser/naming.py を参照して採否を判断した結果:
  * 採用: NFKC・空白除去・算式注記除去・連番(数字/漢数字)除去・空括弧除去・
          末尾算式記号除去・旧字異体字統一。
  * 不採用(旧の挙動を変更): 中黒「・」の無条件削除はしない。
      理由: 正典に「診療・療養等材料費支出」等、中黒が科目名の一部である行が16件あり、
            消すと別概念と衝突しうる。両側同一処理なので残しても照合は成立する。
  * 保持して消さない括弧: 「（公費）」「（一般）」「（特定）」「（保険等査定減）」等、
      意味を区別する括弧は残す(消すと別科目と同一化してしまう)。連番括弧のみ除去。
"""
import re
import unicodedata

# 旧字・異体字の代表的な統一(必要に応じて拡張)
_VARIANT_TABLE = {"齋": "斎", "髙": "高", "﨑": "崎", "渕": "淵"}


def normalize(name):
    """科目名を照合キーへ正規化する。

    正典側・実PDF側の双方で必ずこの関数を通すこと。
    """
    s = name or ""
    # NFKC: 全角英数記号を半角へ、互換文字を正規化
    s = unicodedata.normalize("NFKC", s)
    # 空白除去(全角空白・半角空白・タブ)
    s = s.replace("\u3000", "").replace(" ", "").replace("\t", "")
    # 算式注記の除去: 「＝（３）＋（６）」等。最初の = / ＝ 以降を落とす
    s = re.sub(r"[=＝].*$", "", s)
    # 連番・条項括弧の除去: （１）(1) （一）等。NFKC後は半角数字
    #   注: 公費/一般/特定/保険等査定減 等の意味括弧は数字でないため残る
    s = re.sub(r"[（(][0-9一二三四五六七八九十]+[）)]", "", s)
    # 空括弧の除去: （） () （連番がレイアウトで落ちて空括弧だけ残った場合）
    s = re.sub(r"[（(]\s*[）)]", "", s)
    # 末尾に残った算式記号の除去: (11)＋(12) 形で = を伴わない場合
    s = re.sub(r"[＋+－−\-＝=]+$", "", s)
    # ハイフン類のみ統一(中黒「・」は保持)
    s = s.replace("−", "-").replace("―", "-").replace("─", "-")
    # 旧字・異体字統一
    for a, b in _VARIANT_TABLE.items():
        s = s.replace(a, b)
    return s.strip()
